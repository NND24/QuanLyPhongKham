    private void btnThemQuanLyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemQuanLyActionPerformed
        if (!txtMaQuanLy.getText().isEmpty()) {
            DialogHelper.showError("Quản lý đã tồn tại");
        } else {
            try {
                String hoTen = txtHoTen.getText();
                String gioiTinh = cboGioiTinh.getSelectedItem().toString();
                String diaChi = txtDiaChi.getText();
                String sdt = txtDienThoai.getText();
                String namSinh = txtNamSinh.getText();
                String canCuoc = txtCanCuoc.getText();
                if (hoTen.isEmpty() || namSinh.isEmpty() || canCuoc.isEmpty()) {
                    DialogHelper.showError("Vui lòng điền đầy đủ thông tin");
                } else if (!namSinh.isEmpty() && !Validator.isValidYearOfBirth(namSinh)) {
                    DialogHelper.showError("Năm sinh không hợp lệ");
                } else if (QuanLyCtrl.kiemTraCanCuocCoTonTai(canCuoc)) {
                    DialogHelper.showError("Căn cước đã tồn tại");
                } else if (!sdt.isEmpty() && !Validator.isValidPhoneNumber(sdt)) {
                    DialogHelper.showError("Số điện thoại không hợp lệ");
                } else if (!canCuoc.isEmpty() && !Validator.isValidCccd(canCuoc)) {
                    DialogHelper.showError("Căn cước không hợp lệ");
                } else {
                    QuanLyModel ql = new QuanLyModel(hoTen, gioiTinh, diaChi, namSinh, sdt, canCuoc, currentImage);
                    QuanLyCtrl.themQuanLy(ql);
                    hienThiTatCaQuanLy();
                    DialogHelper.showMessage("Thêm thành công!");
                    lamMoi();
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DSQuanLy.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnThemQuanLyActionPerformed

